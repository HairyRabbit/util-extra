/**@inline */export const MILLISECONDS_OF_SECOND: number = 1000
/**@inline */export const SECONDS_OF_MINUTE: number = 60
/**@inline */export const MINUTES_OF_HOUR: number = 60
/**@inline */export const HOURS_OF_DAY: number = 24
/**@inline */export const MONTHS_OF_YEAR: number = 12
/**@inline */export const DAYS_OF_WEEK: number = 7
/**@inline */export const DAYS_OF_YEAR: number = 365
/**@inline */export const DAYS_OF_LEAPYEAR: number = DAYS_OF_YEAR + 1
/**@inline */export const WEEKS_OF_MONTH: number = 4
/**@inline */export const WEEKS_OF_YEAR: number = 52
/**@inline */export const QUARTERS_OF_YEAR: number = 4

/**@inline */export const MINUTE_SECONDS = MILLISECONDS_OF_SECOND * SECONDS_OF_MINUTE
/**@inline */export const HOUR_SECONDS = MINUTE_SECONDS * MINUTES_OF_HOUR
/**@inline */export const DAY_SECONDS = HOUR_SECONDS * HOURS_OF_DAY
/**@inline */export const WEEK_SECONDS = DAY_SECONDS * DAYS_OF_WEEK

